@Composable
fun TextCharactersLength(string: String, text: String, minLength: Int = DEFAULT_MIN_LENGTH) {
    Row(horizontalArrangement = Arrangement.End,
        modifier = Modifier
            .fillMaxWidth()
            .padding(top = dimen8)) {
        TextMain(text = buildAnnotatedString {
            withStyle(style = SpanStyle(color = if (string.length < minLength) MaterialTheme.colors.error else MaterialTheme.colors.primary)) {
                append((string.length - minLength).toString())
            }
            append(SPACE)
            withStyle(style = SpanStyle(fontWeight = FontWeight.Bold)) {
                append(text)
            }
        })
    }
}